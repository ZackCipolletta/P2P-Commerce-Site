function ButtonWrapper({ currency, showSpinner, product, parentColor }) {
  const [{ options, isPending }, dispatch] = usePayPalScriptReducer();

  useEffect(() => {
    dispatch({
      type: "resetOptions",
      value: {
        ...options,
        currency: currency,
      },
    });
  }, [currency, showSpinner]);

  const amount = (product.price + product.shippingPrice).toFixed(2);
  const style = {
    layout: "vertical",
    color: parentColor, // Set the color of the PayPal buttons to match the parent component's color
    shape: "rect",
    label: "paypal",
  };

  return (
    <>
      {showSpinner && isPending && <div className="spinner" />}
      <PayPalButtons
        style={style}
        disabled={false}
        forceReRender={[amount, currency, style]}
        fundingSource={undefined}
        createOrder={(data, actions) => {
          return actions.order
            .create({
              purchase_units: [
                {
                  amount: {
                    currency_code: currency,
                    value: amount,
                  },
                },
              ],
            })
            .then((orderId) => {
              // Your code here after create the order
              return orderId;
            });
        }}
        onApprove={function (data, actions) {
          return actions.order.capture().then(function () {
            // Your code here after capture the order
          });
        }}
      />
    </>
  );
}


<ButtonWrapper currency={currency} showSpinner={false} product={product} parentColor={parentColor} />
